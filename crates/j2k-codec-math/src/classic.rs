const fn packed_sign_contexts() -> [u16; 256] {
    let mut values = [0; 256];
    let mut index = 0;
    while index < values.len() {
        let (context, xor) = SIGN_CONTEXT_LOOKUP[index];
        values[index] = context as u16 | ((xor as u16) << 8);
        index += 1;
    }
    values
}

