const fn mq_qe_values() -> [u32; 47] {
    let mut values = [0; 47];
    let mut index = 0;
    while index < values.len() {
        values[index] = MQ_STATES[index].qe;
        index += 1;
    }
    values
}

const fn packed_mq_transitions() -> [u32; 47] {
    let mut values = [0; 47];
    let mut index = 0;
    while index < values.len() {
        let state = MQ_STATES[index];
        values[index] =
            state.nmps as u32 | ((state.nlps as u32) << 8) | ((state.switch as u32) << 16);
        index += 1;
    }
    values
}

