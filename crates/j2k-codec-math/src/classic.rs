//! Shared JPEG 2000 classic Tier-1 probability and context tables.

/// One JPEG 2000 MQ probability-state transition.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct ClassicMqState {
    /// Probability estimate.
    pub qe: u32,
    /// Next state after an MPS event.
    pub nmps: u8,
    /// Next state after an LPS event.
    pub nlps: u8,
    /// Whether an LPS event switches the MPS bit.
    pub switch: bool,
}

macro_rules! mq_states {
    ($($qe:expr, $nmps:expr, $nlps:expr, $switch:expr),+ $(,)?) => {
        [$(ClassicMqState { qe: $qe, nmps: $nmps, nlps: $nlps, switch: $switch }),+]
    };
}

